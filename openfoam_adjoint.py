def find_sensitivity_file(run_dir: str) -> Path:
    """Locate the surfacePoints sensitivity output.

    Verified against a live run (openfoam2412): the file is named
    `pointSensNormal<adjointSolverName><suffix>` (here `pointSensNormaladjS1ESI`,
    from optimisationDict's adjoint solver name `adjS1`) and is written as a
    plain `pointScalarField` covering EVERY point of the volume mesh (not a
    curated list of just the design-patch points) — most values are 0, with
    nonzero values at points on/near the sensitivity patch. This is a
    different shape than the naive assumption of a small "list of (point,
    value) pairs on the patch" file; that assumption was wrong and this was
    only discovered by actually running the solver and inspecting its output
    directory, not from documentation.

    We search for `pointSensNormal*` broadly rather than hardcoding the exact
    solver-name suffix, and take the newest match (highest write time), so a
    different adjoint solver name in optimisationDict still resolves.
    """
    root = Path(run_dir)
    candidates = [p for p in root.rglob("pointSensNormal*") if p.is_file()]
    if not candidates:
        raise FileNotFoundError(
            f"No pointSensNormal* sensitivity output found under {run_dir}. "
            "Expected a pointScalarField file written by the surfacePoints "
            "sensitivity type, named pointSensNormal<adjointSolverName><suffix>."
        )
    return max(candidates, key=lambda p: p.stat().st_mtime)


def _parse_foam_scalar_list(text: str) -> np.ndarray:
    """Parse the `internalField nonuniform List<scalar> N ( v1 v2 ... vN )`
    block of an OpenFOAM pointScalarField/volScalarField ASCII file.

    Scans for a bare integer count line immediately followed by a `(` line
    (the "N\\n(\\n...\\n)" list convention OpenFOAM uses everywhere), rather
    than a single regex over the whole file, so a boundaryField sub-dict
    elsewhere in the same file can't confuse the match.
    """
    lines = text.splitlines()
    start = None
    count = None
    for i, line in enumerate(lines):
        s = line.strip()
        if s.isdigit() and i + 1 < len(lines) and lines[i + 1].strip() == "(":
            start = i + 2
            count = int(s)
            break
    if start is None or count is None:
        raise ValueError("Could not find a 'N\\n(\\n...values...\\n)' list in the field file")
    values = []
    for line in lines[start:]:
        s = line.strip()
        if s == ")":
            break
        if not s:
            continue
        try:
            values.append(float(s))
        except ValueError:
            continue
    if len(values) != count:
        raise ValueError(f"Expected {count} scalar values, parsed {len(values)}")
    return np.array(values, dtype=np.float64)


def _parse_foam_vector_list(text: str) -> np.ndarray:
    """Parse a `N\\n(\\n(x y z)\\n...\\n)` OpenFOAM vectorField/pointField
    (used for constant/polyMesh/points)."""
    lines = text.splitlines()
    start = None
    count = None
    for i, line in enumerate(lines):
        s = line.strip()
        if s.isdigit() and i + 1 < len(lines) and lines[i + 1].strip() == "(":
            start = i + 2
            count = int(s)
            break
    if start is None or count is None:
        raise ValueError("Could not find a 'N\\n(\\n(x y z)...\\n)' list in the points file")
    rows = []
    for line in lines[start:]:
        s = line.strip()
        if s == ")":
            break
        if not s:
            continue
        m = re.match(r"\(\s*([0-9.eE+-]+)\s+([0-9.eE+-]+)\s+([0-9.eE+-]+)\s*\)", s)
        if m:
            rows.append([float(m.group(1)), float(m.group(2)), float(m.group(3))])
    if len(rows) != count:
        raise ValueError(f"Expected {count} points, parsed {len(rows)}")
    return np.array(rows, dtype=np.float64)


def read_polymesh_points(run_dir: str) -> np.ndarray:
    """Read constant/polyMesh/points -> array of shape (N, 3), index-aligned
    with the sensitivity pointScalarField's internalField (both are per
    mesh-point, same N, same order — this is how OpenFOAM point fields work:
    they're defined directly over the mesh's point list)."""
    path = Path(run_dir) / "constant" / "polyMesh" / "points"
    if not path.is_file():
        raise FileNotFoundError(f"constant/polyMesh/points not found under {run_dir}")
    return _parse_foam_vector_list(path.read_text(encoding="utf-8", errors="replace"))


def parse_sensitivity_points(sensitivity_text: str, points: np.ndarray) -> tuple[np.ndarray, np.ndarray]:
    """Pair the sensitivity pointScalarField's values with mesh point
    coordinates (both index-aligned by mesh point ID). Returns (points[N,3],
    values[N]) covering every mesh point -- including the many zero-valued
    interior points, which is fine: map_sensitivity_to_stl_vertices's nearest-
    neighbour search will naturally prefer the nearby nonzero surface points
    over distant interior ones, and the max-distance guard catches anything
    that goes wrong."""
    values = _parse_foam_scalar_list(sensitivity_text)
    if len(values) != len(points):
        raise ValueError(
            f"Sensitivity field has {len(values)} values but the mesh has "
            f"{len(points)} points -- they must be the same mesh."
        )
    return points, values


def map_sensitivity_to_stl_vertices(
    stl_path: str,
    sens_points: np.ndarray,
    sens_values: np.ndarray,
    max_distance_m: float,
) -> np.ndarray:
    """Nearest-neighbour-map CFD-mesh sensitivity points onto the ORIGINAL
    STL's vertex order, so the result lines up 1:1 with
    `trimesh.load(stl_path).vertices` — the shape Part 1's
    apply_adjoint_sensitivity_symmetric (right_half_sensitivity) requires.

    snappyHexMesh snaps surface points to the geometry within a small
    tolerance, so CFD points are close to but not identical to STL vertices;
    nearest-neighbour is the correct correspondence here, not an exact match.
    Raises if any STL vertex has no CFD sensitivity point within
    max_distance_m — silently assigning a distant, wrong value would corrupt
    the phi update rather than fail loudly.
    """
    from scipy.spatial import cKDTree

    stl_verts = np.array(oc.read_ascii_stl_vertices(stl_path), dtype=np.float64)
    tree = cKDTree(sens_points)
    dist, idx = tree.query(stl_verts, k=1)
    bad = dist > max_distance_m
    if np.any(bad):
        n_bad = int(np.sum(bad))
        raise ValueError(
            f"{n_bad}/{len(stl_verts)} STL vertices have no adjoint sensitivity "
            f"point within {max_distance_m} m. The adjoint mesh may not cover "
            "the full surface, or geometry drifted between the STL and the "
            "meshed case."
        )
    return sens_values[idx]


