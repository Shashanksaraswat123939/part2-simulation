def build_adjoint_ras_properties() -> str:
    """Verified against tutorials/incompressible/adjointOptimisationFoam/
    shapeOptimisation/naca0012/kOmegaSST/lift/constant/adjointRASProperties —
    unlike the SA pairing, kOmegaSST needs no extra coeffs sub-dict here."""
    return oc._header("dictionary", "adjointTurbulenceProperties") + """
adjointRASModel adjointkOmegaSST;

adjointTurbulence on;
"""


def build_adjoint_control_dict(cfg: AdjointRunConfig) -> str:
    end_time = cfg.primal_iters + cfg.adjoint_iters
    write_interval = max(cfg.primal_iters, 1)
    return oc._header("dictionary", "controlDict") + f"""
application     adjointOptimisationFoam;
startFrom       startTime;
startTime       0;
stopAt          endTime;
endTime         {end_time};
deltaT          1;
writeControl    timeStep;
writeInterval   {write_interval};
purgeWrite      2;
writeFormat     ascii;
writePrecision  8;
writeCompression off;
timeFormat      general;
timePrecision   6;
runTimeModifiable true;
"""


def build_optimisation_dict(cfg: AdjointRunConfig, ref_area_half: float) -> str:
    """`singleRun` manager: one primal solve (kOmegaSST) then one adjoint
    solve (adjointkOmegaSST), objective = raw drag force (weight 1 — see
    module docstring for why the business weight is applied in Python, not
    here). residualControl includes k/omega (primal) and ka/wa (adjoint) —
    verified against sbend/turbulent/kOmegaSST/opt, which is the only shipped
    example with the k/omega transport equations actually solved (the
    naca0012 case's residual entries were not fully shown). sensitivityType
    is `surfacePoints` only (the reference tutorials also compute smoothed
    face-based variants for comparison; we only need the per-point one to
    match Part 1's per-vertex contract)."""
    aref = max(ref_area_half, 1e-9)
    u = cfg.reference_speed_mps
    rho = cfg.air_density_kgm3
    return oc._header("dictionary", "optimisationDict") + f"""
optimisationManager singleRun;

primalSolvers
{{
    op1
    {{
        active                 true;
        type                   incompressible;
        solver                 simple;

        solutionControls
        {{
            consistent yes;
            nIters {cfg.primal_iters};
            residualControl
            {{
                "p.*"       1.e-5;
                "U.*"       1.e-5;
                "k.*"       1.e-5;
                "omega.*"   1.e-5;
            }}
        }}
    }}
}}

adjointManagers
{{
    adjManager1
    {{
        primalSolver             op1;
        adjointSolvers
        {{
            adjS1
            {{
                active                 true;
                type                   incompressible;
                solver                 adjointSimple;

                objectives
                {{
                    type incompressible;
                    objectiveNames
                    {{
                        drag
                        {{
                            weight     1.;
                            type       force;
                            patches    (car);
                            direction  (1 0 0);
                            Aref       {aref};
                            rhoInf     {rho};
                            UInf       {u};
                        }}
                    }}
                }}

                ATCModel
                {{
                    ATCModel          standard;
                }}

                solutionControls
                {{
                    consistent yes;
                    nIters {cfg.adjoint_iters};
                    residualControl
                    {{
                        "pa.*"       1.e-5;
                        "Ua.*"       1.e-5;
                        "ka.*"       1.e-5;
                        "wa.*"       1.e-5;
                    }}
                }}
            }}
        }}
    }}
}}

optimisation
{{
    designVariables
    {{
        // Only the per-point sensitivity type is requested (the reference
        // tutorials also compute smoothed face-based variants for
        // comparison; we skip those -- Part 1's contract wants exactly one
        // scalar per surface vertex, which surfacePoints gives directly).
        sensitivityType    surfacePoints;
        patches            (car);
        adjointEikonalSolver
        {{
            tolerance 1.e-5;
            iters     1000;
            epsilon   0.1;
        }}
    }}
}}
"""


def _write(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def _write_adjoint_fields(zero_dir: Path) -> None:
    """Ua/pa/ka/wa for MY 7-patch domain (inlet, outlet, symmetry, outer,
    upperWall, lowerWall, car). BC types verified against the shipped
    kOmegaSST adjoint tutorials:
      - wall types (kaqRWallFunction, waWallFunction, adjointWallVelocity):
        naca0012/kOmegaSST/lift and sbend/turbulent/kOmegaSST/opt.
      - inlet/outlet types for a domain with SEPARATE inlet and outlet
        patches (my domain, unlike naca0012's single combined freestream
        patch): sbend/turbulent/kOmegaSST/opt, the only shipped kOmegaSST
        example with distinct Inlet/Outlet patches.
      - pa's outlet type here is adjointFarFieldPressure (not sbend's
        adjointOutletPressure): my domain is an open external-aero far-field
        boundary (matches the topology of the SA motorBike case, which used
        adjointFarFieldPressure), not an enclosed duct exit like sbend.
      - outer/upperWall (slip, non-wall, non-farfield) have no confirmed
        reference for ka/wa; `slip` is used there for consistency with how
        Ua/p/k/omega already treat those patches as generic slip boundaries
        in this project's own (already-verified) forward case.
    """
    _write(zero_dir / "Ua", oc._header("volVectorField", "Ua") + """
dimensions      [0 1 -1 0 0 0 0];
internalField   uniform (0 0 0);
boundaryField
{
    inlet       { type adjointInletVelocity; value uniform (0 0 0); }
    outlet      { type adjointOutletVelocity; value uniform (0 0 0); }
    symmetry    { type symmetryPlane; }
    outer       { type slip; }
    upperWall   { type slip; }
    lowerWall   { type adjointWallVelocity; value uniform (0 0 0); }
    car         { type adjointWallVelocity; value uniform (0 0 0); }
}
""")
    _write(zero_dir / "pa", oc._header("volScalarField", "pa") + """
dimensions      [0 2 -2 0 0 0 0];
internalField   uniform 0;
boundaryField
{
    inlet       { type zeroGradient; }
    outlet      { type adjointFarFieldPressure; value uniform 0; }
    symmetry    { type symmetryPlane; }
    outer       { type zeroGradient; }
    upperWall   { type zeroGradient; }
    lowerWall   { type zeroGradient; }
    car         { type zeroGradient; }
}
""")
    _write(zero_dir / "ka", oc._header("volScalarField", "ka") + """
dimensions      [0 0 0 0 0 0 0];
internalField   uniform 0;
boundaryField
{
    inlet       { type adjointZeroInlet; value uniform 0; }
    outlet      { type adjointOutletKa; value uniform 0; }
    symmetry    { type symmetryPlane; }
    outer       { type slip; }
    upperWall   { type slip; }
    lowerWall   { type kaqRWallFunction; value uniform 0; }
    car         { type kaqRWallFunction; value uniform 0; }
}
""")
    _write(zero_dir / "wa", oc._header("volScalarField", "wa") + """
dimensions      [0 2 -1 0 0 0 0];
internalField   uniform 0;
boundaryField
{
    inlet       { type adjointZeroInlet; value uniform 0; }
    outlet      { type adjointOutletWa; value uniform 0; }
    symmetry    { type symmetryPlane; }
    outer       { type slip; }
    upperWall   { type slip; }
    lowerWall   { type waWallFunction; value uniform 0; }
    car         { type waWallFunction; value uniform 0; }
}
""")


