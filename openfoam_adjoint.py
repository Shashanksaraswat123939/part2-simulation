def invoke_adjoint(
    stl_path: str,
    case_dir: str,
    cfg: Optional[AdjointRunConfig] = None,
    bashrc: Optional[str] = None,
) -> np.ndarray:
    """Full adjoint pipeline: build case, run primal+adjoint, parse
    sensitivity, map onto the original STL's vertex order.

    Returns the RAW (unweighted, weight=1 drag-direction) per-vertex
    dD20_half/dSurface sensitivity array. Applying the project's objective
    weight (w_D20) and ADJOINT_HALF_CAR_SCALING is the caller's job
    (cfd_wrapper.run_half_car_adjoint) — see module docstring for why.
    """
    import os

    cfg = cfg or AdjointRunConfig()
    resolved_bashrc = oc.find_openfoam_bashrc(bashrc)
    if resolved_bashrc is None:
        raise oc.OpenFOAMNotFoundError(
            "No ESI OpenFOAM environment found for the adjoint solve. Set "
            "$WM_PROJECT_DIR or $FOAM_BASHRC, or pass bashrc=... ."
        )
    run_dir = str(Path(case_dir) / "adjoint_runs" / f"run_{os.getpid()}_{abs(hash(stl_path)) % 10_000}")
    build_adjoint_case(run_dir, stl_path, cfg)
    try:
        run_adjoint_stages(run_dir, cfg, resolved_bashrc)
        sens_file = find_sensitivity_file(run_dir)
        mesh_points = read_polymesh_points(run_dir)
        sens_points, sens_values = parse_sensitivity_points(
            sens_file.read_text(encoding="utf-8", errors="replace"), mesh_points
        )
        return map_sensitivity_to_stl_vertices(
            stl_path, sens_points, sens_values, cfg.max_point_match_distance_m
        )
    finally:
        if not cfg.keep_run_dir:
            shutil.rmtree(run_dir, ignore_errors=True)


