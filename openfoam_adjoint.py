"""
openfoam_adjoint.py — drag-adjoint surface sensitivity for the half-car CFD
case, using ESI's `adjointOptimisationFoam`.

This is the piece that makes the OpenFOAM case actually "work with the 3
parts": Part 3's `pipeline_interface.real_bindings.run_adjoint` is a hard
`? UNRESOLVED NotImplementedError` stub (see part3-simulation/pipeline_interface.py),
and Part 1's `phi_updater.apply_adjoint_sensitivity_symmetric` (aka `update_phi`)
requires a `right_half_sensitivity` array with exactly one scalar per vertex of
the right-half trimesh, in the SAME order as `mesh.vertices`. This module
produces that array.

Turbulence pairing: kOmegaSST (primal) / adjointkOmegaSST (adjoint) — matching
the Part 2 spec's own stated turbulence model ("laminar baseline, k-omega SST
for final validation"), so the sensitivity field is computed with the SAME
closure used to validate D20/L/Cm, rather than mixing in an unrelated model.

An earlier version of this module used Spalart-Allmaras, on the mistaken
assumption (from having found only ONE tutorial) that it was the only
primal/adjoint turbulence pairing ESI ships. That was wrong: inspecting the
compiled libadjointOptimisation.so's symbol table shows `adjointkOmegaSST` is
a complete, independently-registered adjointRASModel implementation (same
factory table as adjointSpalartAllmaras) with its own tutorials
(shapeOptimisation/naca0012/kOmegaSST/lift and
shapeOptimisation/sbend/turbulent/kOmegaSST/opt, openfoam2412). Both were
used as the ground truth for the boundary conditions and dict schema below —
not assumed from documentation.

Design:
  * `optimisationManager singleRun` — ONE invocation of adjointOptimisationFoam
    runs the primal solve (SIMPLE, kOmegaSST, nIters primal_iters) and then the
    adjoint solve (adjointSimple, adjointkOmegaSST, nIters adjoint_iters)
    internally, driven entirely by optimisationDict. No separate solver
    binaries to call.
  * The primal U/p/k/omega/nut fields are generated by
    openfoam_case._write_initial_fields (same domain, same patches, same
    wall-function conventions already live-verified for the forward-only
    case) — not duplicated here.
  * The objective is `type force, direction (1,0,0)` (raw drag), weight 1 —
    i.e. the CFD side computes unweighted dD20/dSurface. The project's
    Objective = w_D20 x D20 x ADJOINT_HALF_CAR_SCALING (Part 2's
    adjoint_contract.py) is linear in this, so the business-logic weight and
    the 0.5 half-car scaling are applied in Python afterward, not baked into
    the OpenFOAM dict.
  * `sensitivityType surfacePoints` on the `car` patch gives a sensitivity
    value AT MESH POINTS directly (not per-face) — the right shape for a
    per-vertex array — but those points are snappyHexMesh's own (snapped)
    surface points, not the original STL's vertices. This module
    nearest-neighbour-maps snappy's sensitivity points back onto the ORIGINAL
    STL's vertex coordinates (via scipy.spatial.cKDTree) so the returned
    array lines up 1:1, in order, with `trimesh.load(stl_path).vertices` —
    exactly what Part 1 expects to zip against its own mesh.
"""

from __future__ import annotations

import re
import shutil
from dataclasses import dataclass
from pathlib import Path
from typing import Optional

import numpy as np

import openfoam_case as oc


@dataclass(frozen=True)
class AdjointRunConfig:
    """Config for the drag-adjoint sensitivity solve.

    Turbulence is always kOmegaSST (primal) / adjointkOmegaSST (adjoint) —
    matching Part 2's forward validation model (see module docstring). There
    is no turbulence_model choice here, unlike OpenFOAMRunConfig.
    """

    reference_speed_mps: float = 20.0
    air_density_kgm3: float = 1.225
    kinematic_viscosity_m2s: float = oc._MU_AIR_PA_S / 1.225
    primal_iters: int = 1000
    adjoint_iters: int = 1000
    resolution: str = "medium"
    n_subdomains: int = 1
    keep_run_dir: bool = False
    # Nearest-neighbour matches beyond this distance (m) from any original STL
    # vertex are treated as unmapped and raise, rather than silently pairing
    # a sensitivity value with the wrong point.
    max_point_match_distance_m: float = 5e-3

    def __post_init__(self):
        if self.resolution not in oc.RESOLUTION_REFINEMENT:
            raise ValueError(
                f"resolution must be one of {sorted(oc.RESOLUTION_REFINEMENT)}, "
                f"got {self.resolution!r}"
            )
        if self.reference_speed_mps <= 0:
            raise ValueError("reference_speed_mps must be > 0")
        if self.primal_iters <= 0 or self.adjoint_iters <= 0:
            raise ValueError("primal_iters and adjoint_iters must be > 0")

    def as_forward_config(self) -> oc.OpenFOAMRunConfig:
        """Adapts this config to openfoam_case.OpenFOAMRunConfig so the
        primal U/p/k/omega/nut fields and turbulenceProperties can be
        generated by the same, already-verified functions the forward-only
        case uses — same domain, same patches, same wall functions."""
        return oc.OpenFOAMRunConfig(
            reference_speed_mps=self.reference_speed_mps,
            air_density_kgm3=self.air_density_kgm3,
            kinematic_viscosity_m2s=self.kinematic_viscosity_m2s,
            turbulence_model="kOmegaSST",
            resolution=self.resolution,
            n_subdomains=self.n_subdomains,
        )


