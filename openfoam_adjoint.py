def build_adjoint_case(run_dir: str, stl_path: str, cfg: AdjointRunConfig) -> dict:
    """Generate a complete adjoint case under run_dir. Reuses the geometry
    (bounds, domain box, blockMesh/snappy/surfaceFeatureExtract/meshQuality
    dict builders, and the primal U/p/k/omega/nut/turbulenceProperties
    writers) from openfoam_case.py — only adjointRASProperties, the adjoint
    0/ fields, optimisationDict, and controlDict's application differ from
    the forward-only case."""
    run = Path(run_dir)
    if run.exists():
        shutil.rmtree(run)
    (run / "system").mkdir(parents=True, exist_ok=True)
    (run / "constant" / "triSurface").mkdir(parents=True, exist_ok=True)
    (run / "0").mkdir(parents=True, exist_ok=True)

    forward_cfg = cfg.as_forward_config()

    bounds = oc.stl_bounds(stl_path)
    (x0, y0, z0), (x1, y1, z1) = bounds
    ref_len = max(x1 - x0, 1e-4)
    frontal_area_half = oc.compute_frontal_area_half(stl_path)
    box_min, box_max = oc.domain_box(bounds)
    loc = oc.location_in_mesh(bounds)
    cell_size = max(ref_len / 12.0, 1e-4)

    oc._normalise_solid_name(stl_path, run / "constant" / "triSurface" / "car.stl")

    refinement = oc.RESOLUTION_REFINEMENT[cfg.resolution]

    _write(run / "system" / "blockMeshDict", oc.build_blockmesh_dict(box_min, box_max, cell_size))
    _write(run / "system" / "snappyHexMeshDict",
           oc.build_snappy_dict("car.stl", loc, refinement, add_layers=True))
    _write(run / "system" / "controlDict", build_adjoint_control_dict(cfg))
    _write(run / "system" / "fvSchemes", _FV_SCHEMES_ADJOINT)
    _write(run / "system" / "fvSolution", _FV_SOLUTION_ADJOINT)
    _write(run / "system" / "surfaceFeatureExtractDict",
           oc._header("dictionary", "surfaceFeatureExtractDict") + oc._SURFACE_FEATURE_EXTRACT_BODY)
    _write(run / "system" / "meshQualityDict", oc._MESH_QUALITY_DICT)
    _write(run / "system" / "decomposeParDict", oc._decompose_dict(cfg.n_subdomains))
    _write(run / "system" / "optimisationDict", build_optimisation_dict(cfg, frontal_area_half))
    _write(run / "constant" / "transportProperties", oc.build_transport_properties(forward_cfg))
    _write(run / "constant" / "turbulenceProperties", oc.build_turbulence_properties(forward_cfg))
    _write(run / "constant" / "adjointRASProperties", build_adjoint_ras_properties())

    # Primal fields (U, p, k, omega, nut) -- same domain/patches/wall
    # functions as the already-verified forward-only case.
    oc._write_initial_fields(run / "0", forward_cfg, ref_len)
    # Adjoint fields (Ua, pa, ka, wa).
    _write_adjoint_fields(run / "0")

    return {
        "bounds": bounds,
        "frontal_area_half": frontal_area_half,
        "cell_size_m": cell_size,
        "domain_box": (box_min, box_max),
        "location_in_mesh": loc,
    }


def run_adjoint_stages(run_dir: str, cfg: AdjointRunConfig, bashrc: str, timeout_s: int = 14400) -> dict:
    """surfaceFeatureExtract -> blockMesh -> snappyHexMesh -> checkMesh ->
    adjointOptimisationFoam (single invocation runs primal then adjoint,
    per optimisationDict's singleRun manager)."""
    run = Path(run_dir)
    oc._run("surfaceFeatureExtract", run, bashrc, "surfaceFeatureExtract.log", timeout_s)
    oc._run("blockMesh", run, bashrc, "blockMesh.log", timeout_s)
    if cfg.n_subdomains > 1:
        oc._run("decomposePar -force", run, bashrc, "decomposePar.log", timeout_s)
        oc._run(f"mpirun -np {cfg.n_subdomains} snappyHexMesh -overwrite -parallel",
                run, bashrc, "snappyHexMesh.log", timeout_s)
        checkmesh = oc._run(f"mpirun -np {cfg.n_subdomains} checkMesh -parallel",
                            run, bashrc, "checkMesh.log", timeout_s)
        solver_log = oc._run(f"mpirun -np {cfg.n_subdomains} adjointOptimisationFoam -parallel",
                             run, bashrc, "adjoint.log", timeout_s)
        oc._run("reconstructPar -latestTime", run, bashrc, "reconstructPar.log", timeout_s)
    else:
        oc._run("snappyHexMesh -overwrite", run, bashrc, "snappyHexMesh.log", timeout_s)
        checkmesh = oc._run("checkMesh", run, bashrc, "checkMesh.log", timeout_s)
        solver_log = oc._run("adjointOptimisationFoam", run, bashrc, "adjoint.log", timeout_s)
    return {"checkmesh_log": checkmesh, "solver_log": solver_log}


